package logic.util;

/**
 * A cardinal direction, aka up/left/down/right.
 */
public enum Directions {

    /**
     * Direction equal to Y-.
     */
    UP,

    /**
     * Direction equal to X+.
     */
    RIGHT,

    /**
     * Direction equal to Y+.
     */
    DOWN,

    /**
     * Direction equal to X-.
     */
    LEFT;
}
